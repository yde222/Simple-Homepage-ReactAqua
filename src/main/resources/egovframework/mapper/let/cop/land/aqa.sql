CREATE TABLE `estrgs` (
  `NTT_ID` decimal(20,0) NOT NULL,
  `BBS_ID` char(20) NOT NULL,
  `NTT_NO` decimal(20,0) DEFAULT NULL,
  `NTT_SJ` varchar(2000) DEFAULT NULL,
  `BBS_TY_A_CODE` varchar(45) DEFAULT NULL,
  `BBS_TY_B_CODE` varchar(45) DEFAULT NULL,
  `BBS_TY_C_CODE` varchar(45) DEFAULT NULL,
  `BBS_TY_D_CODE` varchar(45) DEFAULT NULL,
  `BBS_TY_E_CODE` varchar(45) DEFAULT NULL,
  `BBS_TY_F_CODE` varchar(45) DEFAULT NULL,
  `BBS_TY_G_CODE` varchar(45) DEFAULT NULL,
  `BBS_TY_H_CODE` varchar(45) DEFAULT NULL,
  `FRST_REGISTER_ID` varchar(45) DEFAULT NULL,
  `FRST_REGIST_PNTTM` datetime DEFAULT NULL,
  `RDCNT` decimal(10,0) DEFAULT NULL,
  `SORT_ORDR` decimal(8,0) DEFAULT NULL,
  `NTCR_ID` varchar(20) DEFAULT NULL,
  `NTCR_NM` varchar(20) DEFAULT NULL,
  `NTT_CN` varchar(2000) DEFAULT NULL,
  `ATCH_FILE_ID` varchar(45) DEFAULT NULL,
  `LAST_UPDT_PNTTM` datetime DEFAULT NULL,
  `LAST_UPDUSR_ID` varchar(45) DEFAULT NULL,
  `USE_AT` char(1) NOT NULL,
  PRIMARY KEY (`NTT_ID`,`BBS_ID`),
  KEY `ESTRGS_ibfk_1_idx` (`BBS_ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE `estrgsdtl` (
  `DTL_ID` char(20) NOT NULL,
  `NTT_ID` decimal(20,0) NOT NULL,
  `BBS_ID` char(20) NOT NULL,
  `ZIP_CD` varchar(45) DEFAULT NULL,
  `ROAD_BASE_AD` varchar(2000) DEFAULT NULL,
  `ROAD_BASE_A` varchar(45) DEFAULT NULL,
  `ROAD_BASE_B` varchar(45) DEFAULT NULL,
  `ROAD_BASE_C` varchar(45) DEFAULT NULL,
  `ROAD_BASE_D` varchar(45) DEFAULT NULL,
  `PRCL_ADRE` varchar(2000) DEFAULT NULL,
  `STATE` varchar(20) DEFAULT NULL,
  `ADRES_STAT` char(4) DEFAULT NULL,
  `FRST_REGISTER_ID` varchar(45) DEFAULT NULL,
  `FRST_REGIST_PNTTM` varchar(45) DEFAULT NULL,
  `MODIFY` char(2) DEFAULT NULL,
  `DEL` char(2) DEFAULT NULL,
  `ERR_REASON` varchar(30) DEFAULT NULL,
  PRIMARY KEY (`DTL_ID`,`NTT_ID`,`BBS_ID`),
  KEY `ESTDTLNTT_ibfk_1_idx` (`NTT_ID`),
  KEY `ESTDTLBBS_ibfk_1_idx` (`BBS_ID`),
  CONSTRAINT `ESTDTLBBS_ibfk_1` FOREIGN KEY (`BBS_ID`) REFERENCES `estrgs` (`BBS_ID`) ON DELETE RESTRICT ON UPDATE RESTRICT,
  CONSTRAINT `ESTDTLNTT_ibfk_1` FOREIGN KEY (`NTT_ID`) REFERENCES `estrgs` (`NTT_ID`) ON DELETE RESTRICT ON UPDATE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;


